void GenerateShadowMap()
{
	// Make a temporary array
	u16* shadowMap = new u16[32*32];
	u16 newX = 0;
	u16 newY = 0;
	for(u16 x = 32; x > 0; x--)
	{
		for(u16 y = 0; y < 32; y++)
		{
			
			// Find terrain pieces and build down and right off of them.
			// We have two pieces - slant and square.
			
			// For something like this:
			//
			// +--------+
			// |        |
			// |        |
			// +--------+
			
			// We want the shadows to slant off of the top-rightmost corner down to the ground (or possibly just a set distance with other slants later)
			// The rest of the body will trail the black shadow squares downwards.
			if(GetTile(28, x, y) == 2)
			{
				//Topright corner is 2(x2) (there will be other test cases...)
				newX = x+1;
				newY = y;
				
				// The first tile is immediately to the right of this tile.
				 // 1 for slant, 2 for solid, 0 for clear
				shadowMap[newX+(newY*32)] = 1;		 
				
				for(u16 shadowCount = newY+1; shadowCount < 32; shadowCount++)
				{
					shadowMap[newX+(shadowCount*32)] = 2;
				}
				
				while(newX < 32 && newY < 32) // Draw the slanted shadows to the ground, provided there are no full black areas here already
				{
					newX++;
					newY++;
					if(shadowMap[newX+(newY*32)] != 2)
					{
						shadowMap[newX+(newY*32)] = 1;
					}
					// And draw the square shadows directly under each of the slanted pieces to the bottom
					for(u16 shadowCount = newY+1; shadowCount < 32; shadowCount++)
					{
						shadowMap[newX+(shadowCount*32)] = 2;
					}
				}
			
			
			
			}
						
			// Bottom Edges
			if((GetTile(28, x, y) == 17 || GetTile(28, x, y) == 18) && y > 1)
			{
				for(u16 shadowCount = y; shadowCount < 32; shadowCount++)
				{
					shadowMap[x+(shadowCount*32)] = 2;
				}
			}
			
			if(GetTile(28, x, y) == 16) // Reverse slant - anything under this slant is clear again.
			{
				
				newX = x;
				newY = y+1;
				
				// The first tile is immediately to the right of this tile.
				shadowMap[newX+(newY*32)] = 3;		 
				// And draw the square shadows directly under each of the slanted pieces to the bottom
				for(u16 shadowCount = newY+1; shadowCount < 32; shadowCount++)
				{
					shadowMap[newX+(shadowCount*32)] = 0;
				}
				
				while(newX < 32 && newY < 32) // Draw the slanted shadows to the ground
				{
					newX++;
					newY++;
					shadowMap[newX+(newY*32)] = 3;
					// And draw the square shadows directly under each of the slanted pieces to the bottom
					for(u16 shadowCount = newY+1; shadowCount < 32; shadowCount++)
					{
						shadowMap[newX+(shadowCount*32)] = 0;
					}
				}				
			}
			
			
			
		}
	}	
	
	//Copy to BG2
	memcpy(&se_mem[29][0], shadowMap, sizeof(u16)*32*32);
}


Level::Level(u16** LevelData, Player* _thePlayer, Vector3D startLocation, std::vector<Vector3D*> eyebotLocations, std::vector< std::vector<Vector3D> > eyebotPaths, Vector3D diskLocation)
{
	// Make visible on screen
	CopyLevelToScreenblock(28, LevelData);
	
	thePlayer = _thePlayer;
	thePlayer->Position = startLocation;
	// Create a disk!
	// theDisk = new Disk(diskLocation);
	
	for(int i = 0; i < eyebotLocations.size(); i++)
	{
		// Create an eyebot and have it follow the path of points that was passed in!
		EyeBot* thisEyebot = new EyeBot(eyebotLocations[i]->X, eyebotLocations[i]->Y, 6, 6, (i+2)*2, thePlayer);
		for(int j = 0; j < eyebotPaths[i].size(); j++)
		{
			Vector3D newPoint; // Add navigation point
			newPoint.X = eyebotPaths[i][j].X;
			newPoint.Y = eyebotPaths[i][j].Y;
			thisEyebot->pathPoints.push_back(newPoint);
		}

		// Add to list!
		eyebotInstances.push_back(thisEyebot);
	}
}

